package com.continentalquality.model;

public class Part {

    private String partNumber;
    private String partDescription;
    private String partPackaging;
    private String partPerBox;
    private String partPerSkid;
    private String partWeightClass;
    private String partPiecePrice;
    private String partMutilation;
    private String partPlating;
    private String partMixed;
    private String partNoGo;
    private String partBoxOnly;
    private String partComments;

    public String getPartNumber() {
        return partNumber;
    }

    public void setPartNumber(String partNumber) {
        this.partNumber = partNumber;
    }

    public String getPartDescription() {
        return partDescription;
    }

    public void setPartDescription(String partDescription) {
        this.partDescription = partDescription;
    }

    public String getPartPackaging() {
        return partPackaging;
    }

    public void setPartPackaging(String partPackaging) {
        this.partPackaging = partPackaging;
    }

    public String getPartPerBox() {
        return partPerBox;
    }

    public void setPartPerBox(String partPerBox) {
        this.partPerBox = partPerBox;
    }

    public String getPartPerSkid() {
        return partPerSkid;
    }

    public void setPartPerSkid(String partPerSkid) {
        this.partPerSkid = partPerSkid;
    }

    public String getPartWeightClass() {
        return partWeightClass;
    }

    public void setPartWeightClass(String partWeightClass) {
        this.partWeightClass = partWeightClass;
    }

    public String getPartPiecePrice() {
        return partPiecePrice;
    }

    public void setPartPiecePrice(String partPiecePrice) {
        this.partPiecePrice = partPiecePrice;
    }

    public String getPartMutilation() {
        return partMutilation;
    }

    public void setPartMutilation(String partMutilation) {
        this.partMutilation = partMutilation;
    }

    public String getPartPlating() {
        return partPlating;
    }

    public void setPartPlating(String partPlating) {
        this.partPlating = partPlating;
    }

    public String getPartMixed() {
        return partMixed;
    }

    public void setPartMixed(String partMixed) {
        this.partMixed = partMixed;
    }

    public String getPartNoGo() {
        return partNoGo;
    }

    public void setPartNoGo(String partNoGo) {
        this.partNoGo = partNoGo;
    }

    public String getPartBoxOnly() {
        return partBoxOnly;
    }

    public void setPartBoxOnly(String partBoxOnly) {
        this.partBoxOnly = partBoxOnly;
    }

    public String getPartComments() {
        return partComments;
    }

    public void setPartComments(String partComments) {
        this.partComments = partComments;
    }
}
