package com.continentalquality.model;

public class Tally {

    private int tallyId;
    private int tallyLotId;
    private String tallyDate;
    private int tallyMutilation;
    private int tallyPlating;
    private int tallyMixed;
    private int tallyNoGo;
    private int tallyOverflow;
    private int tallyTableNumber;

    public int getTallyId() {
        return tallyId;
    }

    public void setTallyId(int tallyId) {
        this.tallyId = tallyId;
    }

    public int getTallyLotId() {
        return tallyLotId;
    }

    public void setTallyLotId(int tallyLotId) {
        this.tallyLotId = tallyLotId;
    }

    public String getTallyDate() {
        return tallyDate;
    }

    public void setTallyDate(String tallyDate) {
        this.tallyDate = tallyDate;
    }

    public int getTallyMutilation() {
        return tallyMutilation;
    }

    public void setTallyMutilation(int tallyMutilation) {
        this.tallyMutilation = tallyMutilation;
    }

    public int getTallyPlating() {
        return tallyPlating;
    }

    public void setTallyPlating(int tallyPlating) {
        this.tallyPlating = tallyPlating;
    }

    public int getTallyMixed() {
        return tallyMixed;
    }

    public void setTallyMixed(int tallyMixed) {
        this.tallyMixed = tallyMixed;
    }

    public int getTallyNoGo() {
        return tallyNoGo;
    }

    public void setTallyNoGo(int tallyNoGo) {
        this.tallyNoGo = tallyNoGo;
    }

    public int getTallyOverflow() {
        return tallyOverflow;
    }

    public void setTallyOverflow(int tallyOverflow) {
        this.tallyOverflow = tallyOverflow;
    }

    public int getTallyTableNumber() {
        return tallyTableNumber;
    }

    public void setTallyTableNumber(int tallyTableNumber) {
        this.tallyTableNumber = tallyTableNumber;
    }
}
